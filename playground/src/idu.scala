		DIV     -> true.B,
		DIVU    -> true.B,
		MULT    -> true.B,
		MULTU   -> true.B
	))

	io.contr.trans_hi := Lookup(io.in.inst, false.B, Array(
		MFHI    -> true.B,
		MTHI    -> true.B
	))

