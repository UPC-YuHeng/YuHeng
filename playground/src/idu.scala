class idu_in extends Bundle {
	val inst = UInt(32.W)
}

class idu_out extends Bundle {
	val rs  = UInt(5.W)
	val rt  = UInt(5.W)
	val rd  = UInt(5.W)
	val imm = UInt(32.W)
}

class idu_contr extends Bundle {
	val aluop    = UInt(4.W)
	val hilo_en  = Bool()
	val trans_hi = Bool()
	val trans_lo = Bool()
}

class idu extends Module {
	val io = IO(new Bundle {
		val in    = Input(new idu_in())
		val out   = Output(new idu_out())
		val contr = Output(new idu_contr())
	})

	io.contr.aluop := Lookup(io.in.inst, alu_nop, Array(
		ADD     -> alu_adds,
		ADDI    -> alu_adds,
		ADDU    -> alu_addu,
		ADDIU   -> alu_addu,
		SUB     -> alu_subs,
		SUBU    -> alu_subu,
		SLT     -> alu_nop,  // TODO
		SLTI    -> alu_nop,  // TODO
		SLTU    -> alu_nop,  // TODO
		SLTIU   -> alu_nop,  // TODO
		DIV     -> alu_divs,
		DIVU    -> alu_divu,
		MULT    -> alu_mults,
		MULTU   -> alu_multu,
		AND     -> alu_and,
		ANDI    -> alu_and,
		LUI     -> alu_nop,  // TODO
		NOR     -> alu_nor,
		OR      -> alu_or,	
		ORI     -> alu_or,	
		XOR     -> alu_xor,
		XORI    -> alu_xor,
		SLLV    -> alu_sftl,
		SLL     -> alu_sftl,
		SRAV    -> alu_sftrs,
		SRA     -> alu_sftrs,
		SRLV    -> alu_sftru,
		SRL     -> alu_sftru,
		BEQ     -> alu_nop,  // TODO
